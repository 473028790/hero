void PWM_Servo_Start(uint8_t angle)
{
  //0.5ms -- 195 -- 0'
  //1.0ms -- 190 -- 45'
  //1.5ms -- 185 -- 90'
  //2.0ms -- 180 -- 135'
  //2.5ms -- 175 -- 180'
  uint8_t compare = 185;
  if(0 == angle)
  {
    compare = 195;
  }
  else if(45 == angle)
  {
    compare = 190;
  }
  else if(90 == angle)
  {
    compare = 185;
  }
  else if(135 == angle)
  {
    compare = 180;
  }
  else if(180 == angle)
  {
    compare = 175;
  }
  __HAL_TIM_SET_COMPARE(&htim8,TIM_CHANNEL_3,compare);
  HAL_TIM_PWM_Start(&htim8,TIM_CHANNEL_3);
}
void PWM_Servo_Stop()
{
  HAL_TIM_PWM_Stop(&htim8,TIM_CHANNEL_3);
}
